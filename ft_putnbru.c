#include "ft_printf.h"

void	ft_putnbru(unsigned int nb)
{
	if (nb >= 0 && nb <= 9)
		ft_putchar(nb + 48);
	else
	{
		ft_putnbru(nb / 10);
		ft_putnbru(nb % 10);
	}
}
